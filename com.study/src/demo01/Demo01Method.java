/*
  方法其实就是若干语句的功能集合
  方法好比一个工厂
  蒙牛工厂    原料：奶牛、饲料、水
             产出：奶制品
  钢铁工厂    原料：铁矿石、煤炭
             产出物：钢铁建材

  参数（原料）:进入方法的数据
  返回值（产出物）：从方法中出来的数据

  定义方法的完整格式：
  修饰符  返回值类型  方法名称（参数类型 参数名称，....）{
        方法体
        return 返回值;
  }

修饰符：现阶段固定写法 public static
返回值类型：也就是方法最终产生的数据结果是什么类型
方法名称：小驼峰
参数类型：进入方法的数据是什么类型
参数名称：进入方法的数据对应的变量名称
        */
